#include "Span.hpp"

int main()
{
	{
		Span sp = Span(50);
		int arr[] = { 5, 3, 2, 6, 7, 8, 9, 73, 53, 33 };
		int n = sizeof(arr) / sizeof(arr[0]);
	
		std::vector<int> vect(arr, arr + n);
		sp.addNumber(vect.begin(), vect.end());
		std::cout << sp.shortestSpan() << std::endl;
		std::cout << sp.longestSpan() << std::endl;
	}
	{
		Span sp = Span(5);
		sp.addNumber(6);
		sp.addNumber(3);
		sp.addNumber(17);
		sp.addNumber(9);
		sp.addNumber(11);
		std::cout << sp.shortestSpan() << std::endl;
		std::cout << sp.longestSpan() << std::endl;
	}
}