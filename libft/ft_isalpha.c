int		ft_str_is_alpha(char *str)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			;
		else if ('a' <= str[i] && str[i] <= 'z')
			;
		else
			return (0);
	}
	return (1);
}
